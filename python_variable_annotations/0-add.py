#!/usr/bin/env python3

def add(a: float, b: float) -> float:
    """
    Adds two floats and returns the result as a float.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The sum of a and b.
    """
    return a + b

if __name__ == "__main__":
    add(1.11, 2.22)